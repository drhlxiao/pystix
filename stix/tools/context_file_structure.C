/**
 * @brief Function called when received TC(22,3) accept context segment from OBMM
 *
 * @param in_tcRawData raw data of the packet received as TC(22,3)
 * @return FSW_OK on success
 *         FSW_ERROR when context segment exceeds max segment number
 */
fsw_ret_e ContextMgmt_acceptContext(uint8_t* in_tcRawData)
{
	uint32_t contextSegment = GET16(&in_tcRawData[TCDATA_22_3_CONTEXT_SEGMENT_POS]);
	if ((contextSegment > MAX_CONTEXT_SEGMENT) || (contextSegment == 0))
	{
		TRACE_ERROR("Wrong context segment number");
		return FSW_ERROR;
	}

	// if first segment erase context segment mask
	if (contextSegment == CONTEXT_FIRST_SEGMENT)
	{
		contextLocalLive_so.tc22_3_contextSegmentMask = 1;
	}
	else
	{
		contextLocalLive_so.tc22_3_contextSegmentMask |= utils_ele2mask(contextSegment - 1);
	}


	//packet length is DATA header + CRC + APP DATA -1 :: 4 + 2 + APPDATA -1 = 5 + APPDATA
	//2B are for segment
	uint16_t appDataSize = (uint16_t)(GET16(&in_tcRawData[4]) - 5 - 2);

	if ((appDataSize + (contextSegment - 1) * MAX_DATA_SIZE) > CONTEXT_LENGTH)
	{
		TRACE_ERROR("data in context segment are too large");
		return FSW_ERROR;
	}

	memcpy(&contextDataLive[(contextSegment - 1) * MAX_DATA_SIZE], &in_tcRawData[TCDATA_22_3_CONTEXT_DATA_POS], appDataSize);

	// in case of last segmnent
	if (contextSegment == CONTEST_LAST_SEGMENT)
	{
		// check if all segments have been received
		if (contextLocalLive_so.tc22_3_contextSegmentMask == (utils_ele2mask(contextSegment) - 1))
		{
			// full context accepted
			uint16_t newCrc = Common_crc16(contextDataLive, CONTEXT_LENGTH, 0xffff);

			// compare ECC
			if (newCrc != contextLocalLive_so.initContextCrc)
			{
				// new and old context are different

			   // update parameters according to context
				context_createParametersFromContext();

				// store new ECC array
				contextLocalLive_so.initContextCrc = newCrc;

				store_new_context();
			}

			// otherwise contexts are identical
		}
		else
		{
			// when processing LAST segment, we are missing 1 or more previous segments
			TMForm_missingContextSegment(contextLocalLive_so.tc22_3_contextSegmentMask, (utils_ele2mask(contextSegment) - 1));
		}
	}

	return FSW_OK;
}

static void ContextMgmt_saveLiveToBackupContext(void)
{
	TRACE("Saving live parameter table to backup");
	memcpy(&contextDataBackup[0], &contextDataLive[0], CONTEXT_LENGTH);

	contextLocalBackup_so = contextLocalLive_so;

	memcpy(contextTableBackup, contextTableLive, sizeof(contextTableLive));
}

void ContextMgmt_resetBackupToLiveContext(void)
{
	TRACE("Resetting live parameter table with backup");
	memcpy(&contextDataLive[0], &contextDataBackup[0], CONTEXT_LENGTH);

	contextLocalLive_so = contextLocalBackup_so;

	memcpy(contextTableLive, contextTableBackup, sizeof(contextTableBackup));

	context_createParametersFromContext();
}

bool_t foundPreInitContext0 = FALSE;
bool_t foundPreInitContext1 = FALSE;
bool_t preInitRun = FALSE;

void ContextMgmt_readOnlyPreInit(void)
{
	TRACE("Context RO PRE init for VERSION %u", CONTEXT_VERSION);

	// zero context
	memset(&contextDataLive[0], 0, CONTEXT_LENGTH);

	// table initialisation
	contextTableLive[0].bitOffset = 0;
	contextTableLive[0].bitSize = ContextMgmt_paramBitSize[0];

	for (uint32_t i = 1; i < LENGTH(contextTableLive); i++)
	{
		contextTableLive[i].bitOffset = contextTableLive[i - 1].bitOffset + contextTableLive[i - 1].bitSize;
		contextTableLive[i].bitSize = ContextMgmt_paramBitSize[i];
	}

	TRACE("Context size: %u", contextTableLive[(uint32_t)ContextMgmt_param_last - 1].bitSize + contextTableLive[(uint32_t)ContextMgmt_param_last - 1].bitOffset);

	FSreadInitialContextFileViaSidechannel(&contextDataLive[0], &foundPreInitContext0, &foundPreInitContext1);

	if (foundPreInitContext0 || foundPreInitContext1)
	{
		TRACE("Found initial context for setup (CTX0: %u, CTX1: %u). Making context active!", foundPreInitContext0, foundPreInitContext1);

		// Calculate ECC to find out later if context was changed
		contextLocalLive_so.initContextCrc = Common_crc16(contextDataLive, CONTEXT_LENGTH, 0xffff);

		TRACE("Calculated the following CRC for PRE init context: %X", contextLocalLive_so.initContextCrc);

		// overwrite parameters with context
		context_createParametersFromContext();
	}
	else
	{
		TRACE("Did not find initial context. Leaving default parameters active and recreating context from defaults.");

		// prepare a context from the default parameters
		// *** DO NOT STORE THE CONTEXT TO FLASH! ***
		context_createContextFromParameters();
		contextLocalLive_so.initContextCrc = Common_crc16(contextDataLive, CONTEXT_LENGTH, 0xffff);
	}

	// in any case, save live context to backup
	ContextMgmt_saveLiveToBackupContext();

	// just ensuring we do not erase the memory again or twice
	preInitRun = TRUE;

	TRACE("Context RO PRE init finished");
}

/**
 * @brief Function called to initialise context
 *
 * Function called to initialise context, need to wait for initialised filessytem.
 * NB: ContextMgmt_readOnlyPreInit() must be called before this init!
 */
void ContextMgmt_init(void)
{
	ASSERT(preInitRun);

	MemFilesystem_filename_s context0FileName = FScreateFilename(E_MEMFILESYSTEM_FILETYPE_CONTEXT0, 0, 0, 0xFFFF, CONTEXT_VERSION);
	MemFilesystem_filename_s context1FileName = FScreateFilename(E_MEMFILESYSTEM_FILETYPE_CONTEXT1, 0, 0, 0xFFFF, CONTEXT_VERSION);

	// we found a context during PRE init
	if (foundPreInitContext0 || foundPreInitContext1)
	{
		uint16_t preInitContextCrcModified = Common_crc16(contextDataLive, CONTEXT_LENGTH, 0xffff);

		TRACE("Calculated the following CRC for PRE init context MODIFIED: %X", preInitContextCrcModified);

		if (contextLocalLive_so.initContextCrc == preInitContextCrcModified)
		{
			TRACE("PRE init context was not modified.");

			// Check if one of the context files needs to be restored
			if (!foundPreInitContext0)
			{
				TRACE("Context 0 was missing --> store loaded context to flash.");
				fsw_ret_e error = FSwrite(&context0FileName, contextDataLive, CONTEXT_LENGTH);
				TMForm_contextFileLoadingProblem(error != FSW_OK, TRUE);
			}

			if (!foundPreInitContext1)
			{
				TRACE("Context 1 was missing --> store loaded context to flash.");
				fsw_ret_e error = FSwrite(&context1FileName, contextDataLive, CONTEXT_LENGTH);
				TMForm_contextFileLoadingProblem(TRUE, error != FSW_OK);
			}

			// no ECC update needed as nothing has changed

			// save (update) shadow copy
			ContextMgmt_saveLiveToBackupContext();
		}
		else
		{
			TRACE("PRE init context WAS modified. Need to store update.");

			// this will save both context files
			// not sending any TMs about failed context (as I do above)
			contextLocalLive_so.initContextCrc = Common_crc16(contextDataLive, CONTEXT_LENGTH, 0xffff);
			store_new_context();
		}
	}
	else
	{
		// no context files were found, need to create them from the parameters
		// not necessary to rerun context generation or ECC calculation, we did that already during PRE init
		store_new_context();
		TMForm_contextFileLoadingProblem(TRUE, TRUE);
	}
}

/**
 * @brief Function used to compare parameters with what is loaded in context (in FLASH)
 *
 * Function creates new context array that is compared to the array stored in Flash, and if they differ, new array is stored in FLASH.
 */
void ContextMgmt_deinit(void)
{
	// create new context
	context_createContextFromParameters();
	uint16_t crcArr = Common_crc16(contextDataLive, CONTEXT_LENGTH, 0xffff);

	if (crcArr != contextLocalLive_so.initContextCrc)
	{
		// context created from parameters differ from stored context - store new context
		TRACE("ECC arrays differ, store new context");
		store_new_context();
	}
}

