/**
 * @brief function used to obtain bit string from context
 *
 * Function to obtain bit string from context and put it into array.
 * Output string is aligned to LSb.
 *
 * @param inout_arr byte array that requested bits are stored into
 * @param in_offset offset within context to start reading bits
 * @param in_size   size of bit-string to read
 */
static void get_from_context(uint8_t *inout_arr, uint32_t in_offset, uint32_t in_size)
{
	uint32_t inByte = in_offset / 8;    ///< first byte to read from context (0 == MSB)
	uint32_t inBit = in_offset % 8;    ///< first bit to read from context (0 == MSb)
	uint32_t outByte = 0;                ///< first byte to write to (start from 0th)
	uint32_t outBit = 8 - (in_size % 8);///< in order to have right-aligned (LSb) array, first bit to write to (0 == MSb)
	uint32_t step = 8 - inBit;           ///< step to process

	if (outBit == 8)
	{
		outBit = 0;
	}

	while (in_size)
	{
		step = (step > in_size) ? in_size : step;

		// reading from context
		uint32_t cnt = (contextDataLive[inByte] >> (8 - inBit - step)) & (utils_ele2mask(step) - 1);

		// either this is last iteration or it will be always from the start of byte (1st iteration starts according to offset)
		inBit = 0;
		inByte++;

		// filling output array
		if (outBit + step > 8)
		{
			// split
			uint32_t step1 = 8 - outBit;    // written to 1st byte
			uint32_t step2 = step - step1;  // written to 2nd byte

			inout_arr[outByte] &= ~(utils_ele2mask(step1) - 1);
			inout_arr[outByte] |= (cnt >> step2);

			inout_arr[outByte + 1] &= ~((utils_ele2mask(step2) - 1) << (8 - step2));
			inout_arr[outByte + 1] |= (cnt & (utils_ele2mask(step2) - 1)) << (8 - step2);
		}
		else
		{
			// zero are to be overwritten
			inout_arr[outByte] &= ~((utils_ele2mask(step) - 1) << (8 - step - outBit));
			// file zeroed bits with data
			inout_arr[outByte] |= (cnt << (8 - step - outBit));
		}

		outBit += step;
		if (outBit >= 8)
		{
			outBit -= 8;
			outByte++;
		}

		in_size -= step;
		step = 8;
	}
}

/**
 * @brief function used to write string to context
 *
 * Function to write bit string to context.
 * Input string is aligned to LSb, where 0-th byte of input array already contains data to write, i.e.
 * 13b string stored in 2B has from MSb of MSB 3b spare + 5b of data in MSB + 8b of data in LSB.
 *
 * @param inout_arr byte array that requested bits are stored into
 * @param in_offset offset within context to start reading bits
 * @param in_size   size of bit-string to read
 */
static void set_to_context(uint8_t *in_arr, uint32_t in_offset, uint32_t in_size)
{
	uint32_t outByte = in_offset / 8;   ///< first byte to write to
	uint32_t outBit = 7 - in_offset % 8; ///< first bit to write to (7 == MSb)
	uint32_t inByte = 0;                 ///< first byte to read
	uint32_t step = in_size % 8;           ///< reading step (from input data)
	uint32_t inBit = step - 1;            ///< first bit to read from first byte (7 == MSb)

	// if byte aligned variable
	if (step == 0)
	{
		// set step to byte
		step = 8;
		inBit = 7;
	}

	while (in_size)
	{
		step = (step > in_size) ? in_size : step;

		// reading data - extract data from given byte
		uint8_t cnt = (in_arr[inByte] >> (inBit + 1 - step)) & (utils_ele2mask(step) - 1);

		// prepare variables for extracting from next byte
		inBit = 7;
		inByte++;

		// filling in context
		if (outBit + 1 < step)
		{
			// split into 2 bytes
			uint8_t step1 = outBit + 1;      // written to 1st byte
			uint8_t step2 = step - step1;  // written to 2nd byte

			// write 1st byte (up to LSb)
			contextDataLive[outByte] &= ~(utils_ele2mask(step1) - 1);
			contextDataLive[outByte] |= ((cnt >> step2) & (utils_ele2mask(step1) - 1));

			// write 2nd byte (from MSb)
			contextDataLive[outByte + 1] &= ~((utils_ele2mask(step2) - 1) << (8 - step2));
			contextDataLive[outByte + 1] |= ((cnt & (utils_ele2mask(step2) - 1)) << (8 - step2));

			// finalise for next iteration
			outByte++;
			outBit = 8 - step2 - 1;
		}
		else
		{
			// will be filled in 1 byte
			// zero are to be overwritten
			contextDataLive[outByte] &= ~((utils_ele2mask(step) - 1) << (outBit + 1 - step));
			// file zeroed bits with data
			contextDataLive[outByte] |= (cnt << (outBit + 1 - step));

			// prepare for next writing
			if (step <= outBit)
			{
				outBit -= step;
			}
			else
			{
				outByte++;
				outBit = 7;
			}
		}

		// decrease remaining size by processed step
		in_size -= step;
		step = 8;
	}
}


void CONTEXT2PARAM(ContextMgmt_param_id_e idx, ParamMgmt_param_id_e param_idx, ContextMgmt_param_part_e param_sidx)
{
	ASSERT(contextTableLive[idx].bitSize <= 8 * sizeof(uint32_t));
	if (contextTableLive[idx].bitSize > 8 * sizeof(uint32_t))
	{
		TRACE_ERROR("parameter too big for variable:b=%u", contextTableLive[idx].bitSize);
		return;
	}

	uint32_t u32 = 0;
	get_from_context((uint8_t*)&u32, contextTableLive[idx].bitOffset, contextTableLive[idx].bitSize);

	// 8bits or less -> move from 0th to 3rd byte
	if (contextTableLive[idx].bitSize <= 8)
	{
		u32 >>= 24;
	}
	// 9-16bits -> move from 1st to 3rd byte
	else if (contextTableLive[idx].bitSize <= 16)
	{
		u32 >>= 16;
	}
	// 17-24bits -> move from 2nd to 3rd byte
	else if (contextTableLive[idx].bitSize <= 24)
	{
		u32 >>= 8;
	}
	// otherwise aligned to 4B

	fsw_ret_e retv;
	if (Value == param_sidx)
	{
		retv = ParamMgmt_setParamValue(param_idx, u32);
	}
	else if (Min == param_sidx)
	{
		retv = ParamMgmt_setParamMinValue(param_idx, u32);
	}
	else if (Max == param_sidx)
	{
		retv = ParamMgmt_setParamMaxValue(param_idx, u32);
	}
	else
	{
		TRACE_ERROR("Incorrect param_sidx %u", param_sidx);
		ASSERT(0);
		retv = 0;
	}

	if (FSW_OK != retv)
	{
		TRACE_ERROR("incorrect retval %u for paramId=%u contextId=%u paramOp=%u lower=%u, value=%u, upper=%u, ctxval=%u", retv, param_idx, idx, param_sidx,
			ParamMgmt_getParamMinValue(param_idx),
			ParamMgmt_getParamValue(param_idx),
			ParamMgmt_getParamMaxValue(param_idx),
			u32);
	}
}

void PARAM2CONTEXT(ContextMgmt_param_id_e idx, ParamMgmt_param_id_e param_idx, ContextMgmt_param_part_e param_sidx)
{
	if (contextTableLive[idx].bitSize > 8 * sizeof(uint32_t))
	{
		TRACE_ERROR("parameter too big for variable:b=%u", contextTableLive[idx].bitSize);
		return;
	}

	uint32_t u32;
	if (Value == param_sidx)
	{
		u32 = ParamMgmt_getParamValue(param_idx);
	}
	else if (Min == param_sidx)
	{
		u32 = ParamMgmt_getParamMinValue(param_idx);
	}
	else if (Max == param_sidx)
	{
		u32 = ParamMgmt_getParamMaxValue(param_idx);
	}

	if (contextTableLive[idx].bitSize <= 8)
	{
		u32 <<= 24;
	}
	else if (contextTableLive[idx].bitSize <= 16)
	{
		u32 <<= 16;
	}
	else if (contextTableLive[idx].bitSize <= 24)
	{
		u32 <<= 8;
	}

	set_to_context((uint8_t*)&u32, contextTableLive[idx].bitOffset, contextTableLive[idx].bitSize);
}

static void context_createParametersFromContext(void)
{
	for (uint32_t i = 0; i < LENGTH(ContextMgmt_c2ptable); i++)
		CONTEXT2PARAM(ContextMgmt_c2ptable[i].ctx, ContextMgmt_c2ptable[i].param, ContextMgmt_c2ptable[i].part);

	for (uint32_t iDet = 0; iDet < NUMBER_OF_DETECTORS; iDet++)
	{
		commonFSW_deteReg_s* defaultReg = XRayADCsCtrl_getDefaultRegisters(iDet);

		for (uint32_t iReg = 0; iReg < ASIC_NUMBER_OF_REGISTERS; iReg++)
		{
			// offset due to individual registers
			uint32_t contextOffset = context_register_bit_offset[iReg] + contextTableLive[(uint16_t)ContextMgmt_param_xray01_registers + iDet].bitOffset;

			// size of individual registers
			uint32_t contextRegSize;
			uint32_t pixelCount;
			if ((iReg + 1 == ASIC_REGISTER_3) || (iReg + 1 == ASIC_REGISTER_4) || (iReg + 1 == ASIC_REGISTER_12))
			{
				pixelCount = NUMBER_OF_PIXELS;
				contextRegSize = context_register_bit_size[iReg] / NUMBER_OF_PIXELS;
			}
			else
			{
				pixelCount = 1;
				contextRegSize = context_register_bit_size[iReg];
			}

			// loop over all pixels
			for (uint32_t iPixel = 0; iPixel < pixelCount; iPixel++)
			{
				uint8_t regByte = 0;
				get_from_context(&regByte, contextOffset, contextRegSize);

				if (regByte > 63)
				{
					TRACE_ERROR("incorrect register value: 0x%02x for det %u and register %u", regByte, iDet, iReg);
					ASSERT(0);
					regByte = 63;
				}

				switch (iReg + 1)
				{
				case ASIC_REGISTER_3:
					utils_setReg3(utils_pixelId2channelId[iPixel], regByte, defaultReg->reg03);
					break;
				case ASIC_REGISTER_4:
				case ASIC_REGISTER_12:
					// bit corresponding pixel is already zeroed
					// regByte should have 1bit (T/F) to affect filling in the registers
					defaultReg->ptrs[iReg][3 - (utils_pixelId2channelId[iPixel] / 8)] |= (1u << (utils_pixelId2channelId[iPixel] % 8)) * regByte;
					break;
				default:
					*(defaultReg->ptrs[iReg]) = regByte;
					break;
				}

				contextOffset += contextRegSize;
			}
		}
	}
}

static void context_createContextFromParameters(void)
{
	for (uint32_t i = 0; i < LENGTH(ContextMgmt_c2ptable); i++)
		PARAM2CONTEXT(ContextMgmt_c2ptable[i].ctx, ContextMgmt_c2ptable[i].param, ContextMgmt_c2ptable[i].part);

	for (uint32_t iDet = 0; iDet < NUMBER_OF_DETECTORS; iDet++)
	{
		commonFSW_deteReg_s* defaultReg = XRayADCsCtrl_getDefaultRegisters(iDet);

		for (uint32_t iReg = 0; iReg < ASIC_NUMBER_OF_REGISTERS; iReg++)
		{
			// offset due to individual registers
			uint32_t contextOffset = context_register_bit_offset[iReg] + contextTableLive[(uint16_t)ContextMgmt_param_xray01_registers + iDet].bitOffset;

			// size of individual registers
			uint32_t contextRegSize;
			uint32_t pixelCount;
			if ((iReg + 1 == ASIC_REGISTER_3) || (iReg + 1 == ASIC_REGISTER_4) || (iReg + 1 == ASIC_REGISTER_12))
			{
				pixelCount = NUMBER_OF_PIXELS;
				contextRegSize = context_register_bit_size[iReg] / NUMBER_OF_PIXELS;
			}
			else
			{
				pixelCount = 1;
				contextRegSize = context_register_bit_size[iReg];
			}

			// loop over all pixels
			for (uint32_t iPixel = 0; iPixel < pixelCount; iPixel++)
			{
				uint8_t regByte = 0;
				switch (iReg + 1)
				{
				case ASIC_REGISTER_3:
					regByte = utils_getReg3(utils_pixelId2channelId[iPixel], defaultReg->reg03);
					break;
				case ASIC_REGISTER_4:
				case ASIC_REGISTER_12:
					// bit corresponding pixel is already zeroed
					regByte = 0x1 & (defaultReg->ptrs[iReg][3 - (utils_pixelId2channelId[iPixel] / 8)] >> (utils_pixelId2channelId[iPixel] % 8));
					break;
				default:
					regByte = *(defaultReg->ptrs[iReg]);
					break;
				}

				set_to_context(&regByte, contextOffset, contextRegSize);

				contextOffset += contextRegSize;
			}
		}
	}
}

/**
 * @brief Function stores 2 new context files
 *
 * Function looks if original context exist and erases them, replacing by new contexts
 */
void store_new_context(void)
{
	MemFilesystem_filename_s filename = FScreateFilename(E_MEMFILESYSTEM_FILETYPE_CONTEXT0, 0, 0, 0xFFFF, CONTEXT_VERSION);
	(void)FSdelete(&filename);
	fsw_ret_e e = FSwrite(&filename, &contextDataLive[0], CONTEXT_LENGTH);
	if (e != FSW_OK)
	{
		TRACE_ERROR("storing new context0 failed: err code %u", e);
	}

	filename.filenameArray[FILENAME_BYTE_TYPE_ID] = E_MEMFILESYSTEM_FILETYPE_CONTEXT1;
	(void)FSdelete(&filename);
	e = FSwrite(&filename, &contextDataLive[0], CONTEXT_LENGTH);
	if (e != FSW_OK)
	{
		TRACE_ERROR("storing new context1 failed: err code %u", e);
	}

	ContextMgmt_saveLiveToBackupContext();

	TRACE("context writing DONE for VERSION %u", CONTEXT_VERSION);
}
